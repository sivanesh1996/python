#SETTING SPECIFIC DATATYPE

a=str("hello !")
print(a)
print(type(a))

b=int(20)
print(b)
print(type(b))

c=float(2.22)
print(c)
print(type(c))

d=complex(1j)
print(d)
print(type(d))

e=list(("elephant","e-vehicle"))
print(e)
print(type(e))

f=tuple(("frog","false"))
print(f)
print(type(f))

g=range(7)
print(g)
print(type(g))

h=dict(name="john",age=2)
print(h)
print(type(h))

i=set(("ink","india"))
print(i)
print(type(i))

j=frozenset(("jackfruit","jerry"))
print(j)
print(type(j))

k=bool(4)
print(k)
print(type(k))

l=bytes(1)
print(l)
print(type(l))

m=bytearray(1)
print(m)
print(type(m))

n=memoryview(bytes(1))
print(n)
print(type(n))


